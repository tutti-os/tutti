import { createHash } from "node:crypto";
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDir, "../..");
const schemaPath = resolve(
  repoRoot,
  "packages/agent/daemon/liveprotocol/schema/agent-activity-live-event.schema.json"
);
const canonicalPath = resolve(
  repoRoot,
  "packages/events/protocol/definitions/agent/activity.updated.event.json"
);
const goOutputPath = resolve(
  repoRoot,
  "packages/agent/daemon/liveprotocol/protocol_revision.gen.go"
);
const tsOutputPath = resolve(
  repoRoot,
  "packages/agent/activity-core/src/liveProtocolRevision.gen.ts"
);
const checkOnly = process.argv.includes("--check");

const liveSchema = JSON.parse(readFileSync(schemaPath, "utf8"));
const canonicalDefinition = JSON.parse(readFileSync(canonicalPath, "utf8"));
const canonicalVariants = [
  "session_audit",
  "turn_update",
  "interaction_update"
].map((eventType) => {
  const variant = canonicalDefinition.payloadSchema.oneOf.find(
    (candidate) => candidate?.properties?.eventType?.const === eventType
  );
  if (!variant) {
    throw new Error(`canonical agent activity schema is missing ${eventType}`);
  }
  return variant;
});
const digest = createHash("sha256")
  .update(JSON.stringify({ liveSchema, canonicalVariants }))
  .digest("hex")
  .slice(0, 16);
const revision = `sha256:${digest}`;

const outputs = new Map([
  [
    goOutputPath,
    `// Code generated by tools/scripts/generate-agent-live-protocol.mjs. DO NOT EDIT.

package liveprotocol

const ProtocolRevision = ${JSON.stringify(revision)}
`
  ],
  [
    tsOutputPath,
    `// Code generated by tools/scripts/generate-agent-live-protocol.mjs. DO NOT EDIT.

export const AGENT_ACTIVITY_LIVE_PROTOCOL_REVISION =
  ${JSON.stringify(revision)} as const;
`
  ]
]);

for (const [path, expected] of outputs) {
  if (checkOnly) {
    const actual = readFileSync(path, "utf8");
    if (actual !== expected) {
      throw new Error(
        `${path} is stale; run pnpm generate:agent-live-protocol`
      );
    }
  } else {
    writeFileSync(path, expected);
  }
}
